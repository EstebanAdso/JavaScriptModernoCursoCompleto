//Funciones con Math
console.log(Math.PI) // Numero Pi
console.log(Math.round(3.8)) // Redondear
console.log(Math.floor(3.8)) // Redondea hacia abajo
console.log(Math.ceil(3.1)) // Redondea hacia arriba
console.log(Math.sqrt(144)) // Raiz cuadrada
console.log(Math.abs(-144)) // Valor Absoluto
console.log(Math.pow(8,4)) // Potencia (8 a la potencia de 4)
console.log(Math.min(8,4,3,4,1,2)) // Nos dice cual es el minimo
console.log(Math.max(8,4,3,4,1,2)) // Nos dice cual es el maximo
console.log(Math.random()) //aleatorio