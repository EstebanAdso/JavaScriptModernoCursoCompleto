//Sumar de uno en uno o Restar de uno en uno
let puntaje = 5;

puntaje++ // incremento de uno en uno
puntaje+=2 //incrementar en un numero asignado
puntaje-=4 //restar en un numero asignado
puntaje-- //restar de uno en uno
puntaje*=2 //multiplicar por un numero asignado
puntaje/=3 //dividir por un numero asignado
++puntaje //preincremento

console.log(puntaje)