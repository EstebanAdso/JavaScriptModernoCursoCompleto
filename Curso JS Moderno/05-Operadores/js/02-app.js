//Comparar valores
const numero1 = 20;
const numero2 = "20"
const numero3 = 30;

// = es para asignar un valor
// == es para comparar un valor
// === para comparar estrictamente un valor
console.log(numero1 == numero2) //es igual pero no en el tipo (true)
console.log(numero1 === numero2) //es igual pero no en el tipo (false)

//Diferente
//!= diferente no le importa el tipo
//!== estrictamente diferente si le importa el tipo
console.log(numero1 != numero2)
console.log(numero1 !== numero2)